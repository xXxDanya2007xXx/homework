#!/usr/bin/env python3

import numpy as np


def probability(temp, label):
    probability_hot_day = (temp > 30).mean() * 100
    print(f"{label:<25}: Вероятность температуры > 30°C: {
          probability_hot_day:.2f}%")


print("=== Сравнение распределений температуры ===")

# 1. Равномерное распределение
uniform_temp = np.random.uniform(low=20, high=35, size=10000)
probability(uniform_temp, "Равномерное распределение")

# 2. Нормальное распределение (обрезанное)
normal_temp = np.clip(np.random.normal(27.5, 3, size=10000), 20, 35)
probability(normal_temp, "Нормальное распределение")

# 3. Треугольное распределение
triangular_temp = np.random.triangular(20, 27.5, 35, size=10000)
probability(triangular_temp, "Треугольное распределение")

# Ответы на вопросы
print("\nОтветы на вопросы:")
print("- Наименьшая вероятность жары получается при нормальном распределении, "
      "поскольку температуры группируются вокруг среднего (~27.5°C).")
print("- Треугольное распределение даёт промежуточный результат "
      "между нормальным и равномерным.")
print("- Равномерное распределение — грубое упрощение, "
      "оно переоценивает число экстремально жарких дней (~33%).")
print("- Для погоды реалистичнее использовать нормальное распределение: "
      "редкие экстремумы встречаются только на краях диапазона.")
